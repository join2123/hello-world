//函数名称:void key(void)
//函数功能:按键函数
//入口参数:
//出口参数:
//函数说明:
///////////////////////////////////////////////////
void key(void)      //按键函数
{
///////////////////key_1按键////////////////////////////////
	if(key_1==0)/////////如果按键按下
	{
	  delay10ms();///////延时10ms///越过抖动区7ms
	  if(key_1==0)///////按键的确按下
	    {
		alarm_buzzer();/////按键音
		
				//count++;///////按键计数器计数
		//if(count>=10){count=0;}//////控制计数器最大值;16即为16进制;20即为20进制;但最大计数255，因为count定义的是unsigend char
		while(key_1==0){display_seg_letter();}///////按键等待抬起

		}
	}
///////////////////key_2按键////////////////////////////////
	if(key_2==0)/////////如果按键按下
	{
	  delay10ms();///////延时10ms///越过抖动区7ms
		if(key_2==0)
{
		alarm_buzzer();
		while(key_2==0){display_seg();}///////按键等待抬起
		}
	}
	
	///////////////////key_3按键////////////////////////////////
	if(key_3==0||key_4==0)/////////如果按键按下
	{
	  delay10ms();///////延时10ms///越过抖动区7ms
	  if(key_3==0||key_4==0)///////按键的确按下
	    {
		if(key_3 ==0 )
				{
					alarm_buzzer();
					count=count+1;//每次加几
					if(count >= 20)
					{
						count = 0;						
					}
				}
				if(key_4 == 0)
				{
					alarm_buzzer();
					if(count > 0)
					{
						count--;
					}
					else
						count = 0;
				}
			}
				while(key_3==0||key_4==0){display_seg();}///////按键等待抬起
		}
	}







